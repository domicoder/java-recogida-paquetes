    private void lblCambiarEstadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblCambiarEstadoMouseClicked
        try {
            int fsel = jtPaquetes.getSelectedRow();
            String id_paquete = String.valueOf(jtPaquetes.getValueAt(fsel, 0).toString());

            Entregado entregado = new Entregado();
            Paquete paquete = new Paquete();
            
            if (rbtnEntregado.isSelected() == true) {
                paquete.setEstado(entregado);
                paquete.ejecutarAccion();
                conexionMySQL.cambiarEstado(paquete.getEstadoValido(), id_paquete);
                mostrarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "Compruebe que 'Estado' fue selecionado.\n", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cambiar estado.\n"+e, "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_lblCambiarEstadoMouseClicked

