    public void mostrarDatos(){
        listadoPaquete = conexionMySQL.getPaqueteDisponible();
        dtm= (DefaultTableModel) jtPaquetes.getModel();
        dtm.setNumRows(0);
        for (int i=0;i<listadoPaquete.size();i++){
            Object [] row = {listadoPaquete.get(i).getId_paquete(),listadoPaquete.get(i).getId_cliente(),listadoPaquete.get(i).getDescripcion(),
            listadoPaquete.get(i).getTipoPaquete(),listadoPaquete.get(i).getEstadoValido(),listadoPaquete.get(i).getUbicacion(),listadoPaquete.get(i).getFecha()};
            dtm.addRow(row);
        }
    }
    
